# This a exaple of Stack data structure
heroes = ['Jhankar', 'Eshan','Tutul','Ali Hossain', 'Hero Alom'] # creating the stack named hero


# Remove from Stack. You know, Stack follows LIFO(Last In Fast Out) policy, that means you have to remove the last element of the stack at first.
heroes.pop()

# Add new element in stack
heroes.append('Rabbil Hasan') # Output: ['Jhankar', 'Eshan', 'Tutul', 'Ali Hossain', 'Rabbil Hasan']


#print the stack
print(heroes) # Output: ['Jhankar', 'Eshan', 'Tutul', 'Ali Hossain']
